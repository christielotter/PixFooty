///////////// defines /////////////////////
#define PADDLE_WIDTH 9
#define PADDLE_HEIGHT 9
#define BALL_SIZE 2
#define BALL_SPEED 2

///////////// objects ///////////////////
N5110 lcd(PC_7, PA_9, PB_10, PB_5, PB_3, PA_10);
Joystick joystick(PC_1, PC_0);
DigitalIn buttonA(BUTTON1); //onboard user button
PongEngine pong;
Map _map;

///////////// prototypes ///////////////
void init();
void render();
void welcome();
void game_over();
////////////////////////////////////////


int main() {
    init();      // initialise devices and objects
    welcome();   // waiting for the user to start 
    render();    // first draw the initial frame 
    int fps = 50;
    thread_sleep_for(1000/fps);  // and wait for one frame period - millseconds
    
    
    while (true) {  // keep looping while lives remain
        // read the joystick input and store in a struct
        UserInput input = {joystick.get_direction(),joystick.get_mag()};
        pong.update(input, lcd);   // update the game engine based on input    
        render();                     // draw frame on screen
        thread_sleep_for(1000/fps);         // and wait for one frame period - ms
    }   
    game_over();
}

void init() {
    lcd.init(LPH7366_1);
    lcd.setContrast(0.5);
    joystick.init();
    pong.init(0,10,9,6,0);     // paddle x position, paddle_height,paddle_width,ball_size,speed
}

void render() {  // clear screen, re-draw and refresh
    lcd.clear();  
    pong.draw(lcd);
    lcd.refresh();
}

void welcome() { // splash screen
       // Draw splash screen
    lcd.drawSprite(0, 0, 48, 84, (int*)_map.open_screen[0]);  
    lcd.setContrast(0.5);
    lcd.refresh();
    ThisThread::sleep_for(1000ms);
    
    // Smooth contrast fade-out
    for (float contrast = 0.5; contrast >= 0.1; contrast -= 0.05) {
        lcd.setContrast(contrast);
        ThisThread::sleep_for(100ms);
    }
    
    lcd.clear();
    lcd.setContrast(0.5);
    lcd.printString("   To Start ",0,1);
    lcd.printString(" Press Nucleo ",0,2);
    lcd.printString("  Blue Button ",0,3);
    lcd.refresh();

        while (buttonA.read() == 1){

         ThisThread::sleep_for(100ms);
      
        }
}

void game_over() { // splash screen 
    while (1) {
        lcd.clear();
        lcd.printString("  Game Over ",0,2);  
        lcd.refresh();
        ThisThread::sleep_for(250ms);
        lcd.clear();
        lcd.refresh();
        ThisThread::sleep_for(250ms);
    }
}
